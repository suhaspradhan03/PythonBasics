parrot = "Norwegian Blue"
# Here we are using 'IN' in for loop
for character in parrot:
    print(character)
# character is a variable.
# we are binding the character in parrot by using 'IN'.
# so when we print the character it gives all the values within the string its binded to.
# For loop continues and prints every character till the end of the sting.
